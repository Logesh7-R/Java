package Basics;

public class VarKeyword {
    public static void main(String[] args){
        var num = 12;
        var ch = 'a';
        var dou = 1.1;
        var bool = true;
        var st = "Logesh";
        System.out.println(num);
        System.out.println(ch);
        System.out.println(dou);
        System.out.println(bool);
        System.out.println(st);
    }
}
